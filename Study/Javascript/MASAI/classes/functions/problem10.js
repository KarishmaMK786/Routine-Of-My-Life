// Addition of two numbers by passing two parameters

function Addition(a, b) {
    console.log(a+b);
}

// Substraction of two numbers by passing two parameters

function Substraction (a, b) {
    console.log(a-b);
}

// Multiplication of two numbers by passing two parameters

function Multiplication (a,b) {
    console.log(a*b);
}

function Devision(a,b) {
    console.log(a/b);
}


Addition(2,2);
Substraction(2,2);
Multiplication(2,2);
Devision(2,2);